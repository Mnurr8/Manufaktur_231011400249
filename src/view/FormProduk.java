    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        if(txtCari.getText().isEmpty()) {

        tampilData();
        return;
        }
        model.setRowCount(0);

        ProdukDAO dao =
                new ProdukDAO();

        List<Produk> list =
                dao.cari(
                        txtCari.getText());

        for(Produk p : list) {

            model.addRow(new Object[]{

                p.getKodeProduk(),
                p.getNamaProduk(),
                p.getKategori(),
                p.getStok(),
                p.getKeterangan()

            });

        }
    }//GEN-LAST:event_btnCariActionPerformed

