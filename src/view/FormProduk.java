    List<Produk> list = dao.getAll();

    for (Produk p : list) {

        model.addRow(new Object[]{
            p.getKodeProduk(),
            p.getNamaProduk(),
            p.getKategori(),
            p.getStok(),
            p.getKeterangan()
        });

    }
