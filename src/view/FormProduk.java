    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
            if(txtKodeProduk.getText().isEmpty()
                    || txtNamaProduk.getText().isEmpty()
                    || txtKategori.getText().isEmpty()
                    || txtStok.getText().isEmpty()) {

                JOptionPane.showMessageDialog(
                        this,
                        "Data harus diisi lengkap");

                return;
            }

            Produk produk = new Produk();

            produk.setKodeProduk(
                    txtKodeProduk.getText());

            produk.setNamaProduk(
                    txtNamaProduk.getText());

            produk.setKategori(
                    txtKategori.getText());

            produk.setStok(
                    Integer.parseInt(
                            txtStok.getText()));

            produk.setKeterangan(
                    txtKeterangan.getText());

            ProdukDAO dao =
                    new ProdukDAO();

            if(dao.insert(produk)) {

                JOptionPane.showMessageDialog(
                        this,
                        "Data berhasil disimpan");

                tampilData();

                txtKodeProduk.setText("");
                txtNamaProduk.setText("");
                txtKategori.setText("");
                txtStok.setText("");
                txtKeterangan.setText("");

            } else {

                JOptionPane.showMessageDialog(
                        this,
                        "Data gagal disimpan");

            }
    }//GEN-LAST:event_btnSimpanActionPerformed

